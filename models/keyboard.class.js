/**
 * Represents the keyboard state manager for game controls.
 * Tracks the current status of gameplay-relevant keys.
 *
 * @class
 */
class Keyboard {
    /**
     * Flag for left arrow, up arrow, right arrow, down arrow, space and D key state.
     * @type {boolean}
     */
    LEFT = false;
    RIGHT = false;
    UP = false;
    DOWN = false;
    SPACE = false;
    D = false;
    D_pressed = false;
}